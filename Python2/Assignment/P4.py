# P4.Measurement Conversion

inches = float(input("Enter measurement in inches: "))
foot = inches / 12
yard = inches / 36
centimetre = inches * 2.54
meter = inches / 39.37

print("Measurement in feet:", foot)
print("Measurement in yards:", yard)
print("Measurement in centimetres:", centimetre)
print("Measurement in meters:", meter)
