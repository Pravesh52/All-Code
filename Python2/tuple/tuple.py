# empty tuple
t=()
# tuple having integers
t=(1,2,3,4)
print(t)

# tuple with mixed datatypes
t=(1,'raju',28,'abc')
print(t)

# nested tuple

t=(1,(2,3,4),[1,'raju',28,'abc'])
print(t)

t=('satish',)
type(t)

t="satish",
print(type(t))
print(t)

t=('satish','ramesh','kailash','pravesh')
print(t[0])

# nested tuple

t=('satish',('ramesh','kailash','pravesh'))
print(t[0],t[1])

# slicing
t=(1,2,3,4,5,6)
print(t[1:4])
print(t[:-2])
print(t[:])

